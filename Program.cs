    public static List<int> dynamicArray(int n, List<List<int>> queries)
    {
        List<int> finalAnswer = new List<int>();
        int lastAnswer = 0;
        List<List<int>> newArray = new List<List<int>>();
        for (int i = n; i >= 1; i--)
        {
            newArray.Add(new List<int>());
        }

        foreach (List<int> query in queries)
        {
            int a = query[0];
            int b = query[1];
            int c = query[2];

            int idx = (b ^ lastAnswer) % n;


            if (a == 1)
            {
                newArray[idx].Add(c);

            }

            if (a == 2)
            {
                lastAnswer = newArray[idx][c % newArray[idx].Count];
                finalAnswer.Add(lastAnswer);
            }
        }

        return finalAnswer;
    }


